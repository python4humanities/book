print('a' in ('a', 'b', 'c', 'd'))                    # tuple
print('b' in ['a', 'b', 'c', 'd'])                    # list
print('c' in {'a', 'b', 'c', 'd'})                    # set
print('d' in {'a': 0, 'b': 1, 'c': 2, 'd': 3})        # dict
print('e' in 'abcd')                                  # str
